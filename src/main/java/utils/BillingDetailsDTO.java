package utils;

public class BillingDetailsDTO {
    String firstName;
    String lastName;
    String email;
    String phone;
    String country;
    String address;
    String postalCode;
    String townCity;
    String province;

    public String getFirstName() {
        return firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public String getEmail() {
        return email;
    }

    public String getPhone() {
        return phone;
    }

    public String getCountry() {
        return country;
    }

    public String getAddress() {
        return address;
    }

    public String getPostalCode() {
        return postalCode;
    }

    public String getTownCity() {
        return townCity;
    }

    public String getProvince() {
        return province;
    }
}
